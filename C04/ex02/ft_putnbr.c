#include <unistd.h>

void	ft_rec(int nb)
{
	char	a;
	if (nb > 9)
		ft_rec(nb / 10);
	a = (nb % 10) + '0';
	write(1, &a, 1);
}
	
void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return;
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb *= -1;
	}
	ft_rec(nb);
}
