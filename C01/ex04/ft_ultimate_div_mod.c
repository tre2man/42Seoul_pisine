void	ft_ultimate_div_mod(int *a, int *b)
{
	int		div;
	int		rem;

	if (*b == 0)
	{
		*a = 0;
		*b = 0;
	}
	else
	{
		div = *a / *b;
		rem = *a % *b;
		*a = div;
		*b = rem;
	}
}
